// Build the full extended price hash table at compile time.
// The Go source has 1753..2000 and 201..1752 ranges with specific patterns.
// Rather than listing all 4000+ entries literally, we build a lookup map at runtime
// from the literal entries above plus the pattern-generated entries below.

use std::collections::HashMap;

/// Build the complete HashTablePrice lookup at runtime.
/// The Go source contains literal entries for small deltas (0-200) and then
/// pattern-generated entries for larger deltas. We replicate that here.
pub fn build_price_hash_table() -> HashMap<i32, i32> {
    let mut table = HashMap::new();

    // Insert literal entries
    for item in HASH_TABLE_PRICE {
        table.insert(item.hash_value, item.idx);
    }

    // Entries for idx 1753..2000: hash_value = 0x62AAE000 + (idx - 1753) * 2 for positive,
    // and + 1 for negative
    for delta in 1753i32..=2000 {
        let base = 0x62AAE000i32 + (delta - 1753) * 2;
        table.insert(base, delta);
        table.insert(base + 1, -delta);
    }

    // Entries for idx 201..1752: hash_value pattern = -(0x3AAA3C20) + (idx - 201) * 2 for positive,
    // and + 1 for negative. In Go: -0x3AAA3C20 = -(0x3AAA3C20) = -984235040
    let base_neg = -(0x3AAA3C20i32); // -984235040
    for delta in 201i32..=1752 {
        let hv = base_neg + (delta - 201) * 2;
        table.insert(hv, delta);
        table.insert(hv + 1, -delta);
    }

    table
}

